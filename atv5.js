// regressiva. Peça um número e 
// conte deste número até 0, 
// usando um loop while no 
// console do navegador.

contador = 0
let a = prompt("Informe um número e veja esse numero na descrescente")

while (contador != a){
    contador += 1
    console.log(contador);
    alert(contador)
}
